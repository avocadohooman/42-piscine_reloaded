int	ft_sqrt(int nb)
{
	int m;

	m = 0;
	if (nb == 0)
		return (0);
	while (nb)
	{
		if ((m * m) == nb)
			return (m);
		else if ((m * m) > nb)
			return (0);
		m++;
	}
	return (0);
}
